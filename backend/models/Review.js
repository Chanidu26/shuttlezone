import mongoose from "mongoose";

const ReviewSchema = new mongoose.Schema(
  {
    court: {
      type: mongoose.Types.ObjectId,
      ref: "Court",
    },
    user: {
      type: mongoose.Types.ObjectId,
      ref: "User",
    },
    reviewText: {
      type: String,
      required: true,
    },
    rating: {
      type: Number,
      required: true,
      min: 0,
      max: 5,
      default: 0,
    },
  },
  { timestamps: true }
);

export default mongoose.model("Review", ReviewSchema);